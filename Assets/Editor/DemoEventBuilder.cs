        static EventChoice C(string label, string item, string result, int hunger, int thirst, int pain, int fatigue, int depression, bool bag)
        {
            return new EventChoice
            {
                label = label, requiredItem = item, resultText = result,
                hunger = hunger, thirst = thirst, pain = pain, fatigue = fatigue, depression = depression,
                bagUpgrade = bag
            };
        }

        static void Make(string id, string room, string name, string item, string situation, EventChoice[] choices)
        {
            var ev = ScriptableObject.CreateInstance<EventData>();
            ev.eventId = id; ev.eventName = name; ev.eventType = "특정지역"; ev.region = room;
            ev.relatedItemId = item; ev.situation = situation; ev.choices = choices; ev.probability = 100;
            AssetDatabase.CreateAsset(ev, "Assets/Data/Events/" + id + ".asset");
        }
